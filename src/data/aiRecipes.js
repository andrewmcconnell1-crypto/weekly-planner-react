// These are original recipes synthesised from the common ground of many good
// versions of each dish: safe consensus quantities, plus the techniques the
// well-regarded versions share. Unlike the RecipeTin entries, these are not
// links — they carry their own full method, so the source is "Original recipes"
// and there is no sourceUrl. Every ingredient referenced in a method is listed
// with a quantity (including the oil and seasoning), so nothing is left to guess.
//
// Methods are written to stand alone: each step carries the pan and heat level,
// a time range, a sensory doneness cue, and — where it earns its place — the
// reason a technique matters, so a first-time cook gets the same result as a
// practised one. timeMins is the realistic start-to-plate total (prep,
// marinating and resting included), which also feeds the "Quick" (≤30 min)
// filter.

