// Link-based recipes from other recipe websites.
//
// Like the RecipeTin entries, these carry a curated, simplified ingredient list
// (built so every quantity is usable in the shopping list) plus a link out to
// the original page for the full method — the method itself lives on the source
// site, not here. Each `sourceUrl` was confirmed to exist via web search at the
// time of adding. To add a recipe, prefer one of the sites already here so the
// source badge stays recognisable.

import { deriveMainCategory, deriveRecipeTags } from "../utils/recipeUtils";

const METHOD_NOTE = "Open source link for full method.";

function webRecipe({ id, name, category, source, sourceUrl, ingredients }) {
  return {
    id,
    name,
    category: deriveMainCategory({ name, category, ingredients }),
    source,
    sourceUrl,
    ingredients,
    method: METHOD_NOTE,
    tags: deriveRecipeTags({ name, category, ingredients }),
    timeMins: null,
  };
}

