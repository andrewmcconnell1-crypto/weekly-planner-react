// The app's logotype: the wordmark set in the display serif. Sits where a
// screen's eyebrow label would — one quiet, human signature instead of a
// generic product label. The monogram mark (see BistroMark / the app icon)
// carries the brand where it stands alone; here the word does the work, since
// pairing a “B” with “Bistro” only doubles the initial.
function BrandMark() {
  return (
    <span className="brand-mark">
      <span className="brand-mark-word">Bistro</span>
    </span>
  );
}

export default BrandMark;
